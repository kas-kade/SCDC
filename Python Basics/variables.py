# NUMBERS

# Integer -> Whole Number
myInt = 4
print(myInt)


# Floating Point -> Decimals
myFloat = 4.75
print(myFloat)


# You can also change integers into floats
convertInt = float(4)
print(convertInt)





# STRINGS

# Strings can be defined with either a single or double quotations
# Strings refer to whole letters, words, or sentences 

quote1 = 'hello world'
print(quote1)

quote2 = "hello world"
print(quote2)



# OPERATIONS

one = 1
